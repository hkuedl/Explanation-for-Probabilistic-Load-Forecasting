def split_dataframe(df, column):
    # 使用groupby方法分割数据框
    grouped = df.groupby(column)
    
    # 创建一个字典来存储分割后的数据框
    df_dict = {}
    
    # 遍历groupby对象，将每个分组的数据框存储在字典中
    for name, group in grouped:
        df_dict[name] = group
    
    return df_dict

